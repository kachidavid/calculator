import math

def add(num1, num2):
    return num1 + num2
def subtract(num1, num2):
    return num1 - num2
def divide(num1, num2):
    total = num1 // num2
    rem = num1 % num2
    return (total + 'remainder : {rem}')
def multiply(num1, num2):
    return num1 * num2
def square_root(num1):
    return math.sqrt(num1)





